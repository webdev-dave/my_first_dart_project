void main () {
  // Strings
  String firstName = "John";
  print("String: $firstName");

  // Integers
  int myNum = 41;
  print("Integer: $myNum");

  // Doubles
  double decimalNum = 19.95;
  print("Double: $decimalNum");

  // Booleans
  bool myBool = false;
  print("Boolean: $myBool");

  // Dynamic
  dynamic fullName = "I can be anything... You will find out who I am during runtime ;)";
  print("Dynamic: $fullName");
}